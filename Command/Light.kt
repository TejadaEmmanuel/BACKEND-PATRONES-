package Command

// Receptor (Luz)
class Light {
    fun turnOn() {
        println("Luz encendida")
    }

    fun turnOff() {
        println("Luz apagada")
    }
}

// Receptor (Ventilador)
class Fan {
    fun turnOn() {
        println("Ventilador encendido")
    }

    fun turnOff() {
        println("Ventilador apagado")
    }
}
